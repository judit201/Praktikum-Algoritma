import math

a = float(input("Masukkan nilai a = "))
b = float(input("Masukkan nilai b = "))

hasil = a + b
print("Hasil jumlah a ditambah b adalah", hasil)

hasil = abs(a - b)
print("Hasil selisih antara a dengan b adalah", hasil)

hasil = a * b
print("Hasil kali a dan b adalah", hasil)

hasil = a % b
print("Hasil sisa pembagian a dengan b adalah", hasil)

hasil = a / b
print("Hasil pembagian a dengan b adalah", hasil)

hasil = math.log10(a)
print("Hasil dari log(a) adalah", hasil)

hasil = a ** b
print("Hasil a pangkat b adalah", hasil)

